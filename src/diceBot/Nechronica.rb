  def check_nD10(total_n, dice_n, signOfInequality, diff, dice_cnt, dice_max, n1, n_max)# ゲーム別成功度判定(nD10)
    return '' unless(signOfInequality == ">=")

    if(total_n >= 11)
      return " ＞ 大成功"
    end

    if(total_n >= diff)
      return " ＞ 成功"
    end

    #失敗パターン

    if(n1 <= 0)
      return " ＞ 失敗"
    end

    result = " ＞ 大失敗"
    if( dice_cnt > 1 )
      result += " ＞ 使用パーツ全損"
    end

    return result
  end

