    table = [
             '防御側任意',
             '脚（なければ攻撃側任意）',
             '胴（なければ攻撃側任意）',
             '腕（なければ攻撃側任意）',
             '頭（なければ攻撃側任意）',
             '攻撃側任意',
            ]
    index = dice - 6

    addDamage = ""
    if(dice > 10)
      index = 5
      addDamage = "(追加ダメージ#{dice - 10})"
    end

    output = table[index] + addDamage

    return output
  end
  
